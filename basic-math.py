# + --> toplama
# - --> çıkarma
# * --> çarpma
# / --> bölme
# **--> üs 
# % --> mod 
# //--> tam bölme

print(3+4)

print (3.2+4)

print(2*4)

print(10/5) # bölme işleminde her zaman sonuç tam olsada değer float olarak gösterilir.

print(3+5*2)

print((3+5)*2)

print(3+5*2/2)

print((3+5)*2/6)

# parantez içerisindeki işlem her zaman öncelikli gelir.

# 2 üssü 3
print(2**3)

# karekökünü almış oluyoruz.
print(25**0.5)

# 10 mod 3 yani 10'dan 3'ü bölüyoruz ve bize kalanı veriyor.
print(10%3)

print(10%4)

# eğer bir sayıyı 2'ye bölüyorsak ve kalan 0 ise çift 1 ise tek sayıdır.

# çift sayı.
print(100%2)

# tek sayı
print(101%2)

# print(10//3) normalde tam bölünmemektedir fakat "//"  operatörü tam bölmektedir. Sonuç her zaman integer.
print(10//3)

